# 字符串操作
name = "Python"
print("Hello, " + name)

# 字符串索引和切片
text = "Programming"
print(f"First char: {text[0]}")
print(f"Last char: {text[-1]}")
print(f"Slice [0:7]: {text[0:7]}")

# 字符串方法
message = "hello world"
print(f"Upper: {message.upper()}")
print(f"Replace: {message.replace('world', 'Python')}")

# 多行字符串
doc = """
This is a
multi-line
string
"""
print(doc)
